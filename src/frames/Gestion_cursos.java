    private void cargarTabla() {

        DefaultTableModel modelo = new DefaultTableModel();
        // Definimos solo las columnas de la tabla cursos
        modelo.addColumn("ID");
        modelo.addColumn("Nombre del Curso");

        tablagestioncurso.setModel(modelo);

        // Consulta simple a la tabla curso
        String sql = "SELECT id_curso, nombre_curso FROM curso";

        try {
            Connection con = Conectar.conexion();
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {

                Object[] fila = new Object[2];

                fila[0] = rs.getInt(1);    // id_curso
                fila[1] = rs.getString(2); // nombre_curso

                modelo.addRow(fila);
            }

            rs.close();
            ps.close();
            con.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar cursos: " + e.getMessage());
        }
    }

