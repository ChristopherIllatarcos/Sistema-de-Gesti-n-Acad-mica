    private void btnGenerarRespadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarRespadoActionPerformed

        java.time.LocalDate fecha = java.time.LocalDate.now();
        String ruta = System.getProperty("user.home") + "/Desktop/Respaldo_Completo_" + fecha + ".txt";

        try (Connection cn = Conectar.conexion(); java.io.FileWriter fw = new java.io.FileWriter(ruta); java.io.PrintWriter pw = new java.io.PrintWriter(fw)) {

            pw.println("--- RESPALDO DE SEGURIDAD INTEGRAL ---");
            pw.println("Fecha: " + fecha);
            pw.println("------------------------------------------\n");

            // 1. RESPALDO DE ALUMNOS
            pw.println(">>> TABLA: ALUMNOS <<<");
            ResultSet rsA = cn.prepareStatement("SELECT * FROM alumnos").executeQuery();
            while (rsA.next()) {
                pw.println("ID: " + rsA.getString(1) + " | Nombre: " + rsA.getString(2) + " " + rsA.getString(3));
            }

            pw.println("\n------------------------------------------\n");

            // 2. RESPALDO DE NOTAS (Lo que te faltaba)
            pw.println(">>> TABLA: NOTAS <<<");
            ResultSet rsN = cn.prepareStatement("SELECT * FROM notas").executeQuery();
            while (rsN.next()) {
                // Ajusta los getString(#) según el orden de tus columnas en la tabla notas
                pw.println("ID Nota: " + rsN.getString(1) + " | Alumno ID: " + rsN.getString(2)
                        + " | Tarea: " + rsN.getString(4) + " | Nota: " + rsN.getString(5));
            }

            JOptionPane.showMessageDialog(this, "Respaldo integral generado en el Escritorio.");
            java.awt.Desktop.getDesktop().open(new java.io.File(ruta));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en respaldo: " + e.getMessage());
        }

    }//GEN-LAST:event_btnGenerarRespadoActionPerformed

