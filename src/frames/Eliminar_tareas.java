    private void btnEliminarDatosSeleccionadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarDatosSeleccionadosActionPerformed
        if (opcionSeleccionada == 0) {
            JOptionPane.showMessageDialog(this, "Primero selecciona qué deseas borrar.");
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(this, "¿Estás seguro? No hay vuelta atrás.", "CONFIRMAR", JOptionPane.YES_NO_OPTION);

        if (respuesta == JOptionPane.YES_OPTION) {
            try {
                // El try-with-resources cierra la conexión automáticamente
                try (Connection cn = Conectar.conexion()) {
                    switch (opcionSeleccionada) {
                        case 1 -> {
                            // Borrar solo alumnos
                            PreparedStatement pst = cn.prepareStatement("DELETE FROM alumnos");
                            pst.executeUpdate();
                        }
                        case 2 -> {
                            // Borrar solo notas (tu tabla real se llama 'notas')
                            PreparedStatement pst = cn.prepareStatement("DELETE FROM notas");
                            pst.executeUpdate();
                        }
                        case 3 -> {
                            // ORDEN CORRECTO para evitar errores de llaves foráneas:
                            // 1. Borramos notas (dependen de alumnos y cursos)
                            cn.prepareStatement("DELETE FROM notas").executeUpdate();
                            // 2. Borramos alumnos
                            cn.prepareStatement("DELETE FROM alumnos").executeUpdate();
                            // 3. Borramos cursos
                            cn.prepareStatement("DELETE FROM curso").executeUpdate();
                        }
                        default -> {
                        }
                    }
                }

                JOptionPane.showMessageDialog(this, "¡Datos borrados con éxito!");
                txtConfirmar.setText(""); // Limpiamos el cuadro
                btnEliminarDatosSeleccionados.setEnabled(false); // Bloqueamos el botón de nuevo
                opcionSeleccionada = 0; // Reseteamos la elección

            } catch (HeadlessException | SQLException e) {
                // Esto capturará si vuelve a haber un error de "no such table"
                JOptionPane.showMessageDialog(this, "Error de base de datos: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarDatosSeleccionadosActionPerformed

