    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed

        String nuevaTarea = txttarea.getText().trim();
        String nuevaNotaStr = txtcalificacion.getText().trim();

        if (nuevaTarea.isEmpty() || nuevaNotaStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
        } else {
            try {
                int nuevaNota = Integer.parseInt(nuevaNotaStr);

                // SQL para actualizar usando la tarea original como referencia
                String sql = "UPDATE notas SET tarea = ?, calificacion = ? WHERE id_alumno_nota = ? AND tarea = ?";

                try (Connection con = Conectar.conexion(); PreparedStatement ps = con.prepareStatement(sql)) {

                    ps.setString(1, nuevaTarea);
                    ps.setInt(2, nuevaNota);
                    ps.setInt(3, idAlumnoLocal);
                    ps.setString(4, tareaOriginal);

                    int resultado = ps.executeUpdate();

                    if (resultado > 0) {
                        JOptionPane.showMessageDialog(null, "¡Calificación actualizada con éxito!");
                        this.dispose(); // Cerramos esta ventana y volvemos a la anterior
                    }
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "La calificación debe ser un número entero (10-70)");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al actualizar: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_btnactualizarActionPerformed

