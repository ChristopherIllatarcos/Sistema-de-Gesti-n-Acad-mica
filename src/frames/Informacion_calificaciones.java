    private void cargarTablaCalificaciones() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tarea / Actividad");
        model.addColumn("Calificación");
        tabla_informarcion_calificaciones.setModel(model);

        String sql = "SELECT tarea, calificacion FROM notas WHERE id_alumno_nota = ?";

        try (Connection con = Conectar.conexion(); PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setInt(1, idAlumnoLocal);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Object[] fila = new Object[2];
                fila[0] = rs.getString("tarea");
                fila[1] = rs.getInt("calificacion");
                model.addRow(fila);
            }
        } catch (SQLException e) {
            System.err.println("Error al llenar tabla: " + e.getMessage());
        }
    }

