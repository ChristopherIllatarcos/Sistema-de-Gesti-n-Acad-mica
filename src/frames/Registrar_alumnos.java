    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed

        String nombre = txtnombre.getText().trim();
        String apellidos = txtapellidos.getText().trim();
        String grado = cmbgrado.getSelectedItem().toString();
        String telefono = txttelefono.getText().trim();
        String materia = cmbmateria.getSelectedItem().toString();

        if (nombre.isEmpty() || apellidos.isEmpty() || telefono.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }

        if (cmbmateria.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Seleccione una materia");
            return;
        }

        try {
            Connection con = Conectar.conexion();

            // 1. Buscamos el ID del curso seleccionado
            String sqlCurso = "SELECT id_curso FROM curso WHERE nombre_curso = ?";
            PreparedStatement psCurso = con.prepareStatement(sqlCurso);
            psCurso.setString(1, materia);
            ResultSet rs = psCurso.executeQuery();

            int idCurso = 0;
            if (rs.next()) {
                idCurso = rs.getInt("id_curso");
            }

            rs.close();
            psCurso.close();

            // 2. MODIFICACIÓN: Agregamos id_docente al final del INSERT
            String sql = "INSERT INTO alumnos (nombre, apellidos, grado, telefono, id_curso_asignado, id_docente) VALUES (?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setString(1, nombre);
            ps.setString(2, apellidos);
            ps.setString(3, grado);
            ps.setString(4, telefono);
            ps.setInt(5, idCurso);

            // 3. AQUÍ LA MAGIA: Mandamos el ID del docente logueado
            ps.setInt(6, Login.idUsuarioLogeado);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Alumno registrado correctamente");

            ps.close();
            con.close();

            limpiarCampos();
            cargarTabla(""); // Ahora sí aparecerá porque ya tiene dueño

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

