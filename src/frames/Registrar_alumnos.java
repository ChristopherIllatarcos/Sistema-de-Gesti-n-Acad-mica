    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        String id = txtidalumno.getText();

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un alumno de la tabla");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro que desea eliminar este alumno?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {

            try {

                Connection con = Conectar.conexion();
                String sql = "DELETE FROM alumnos WHERE id_alumno = ?";
                PreparedStatement ps = con.prepareStatement(sql);

                ps.setInt(1, Integer.parseInt(id));

                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Alumno eliminado correctamente");

                ps.close();
                con.close();

                limpiarCampos();
                cargarTabla("");
                tablaregistroalumno.clearSelection();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_btneliminarActionPerformed

