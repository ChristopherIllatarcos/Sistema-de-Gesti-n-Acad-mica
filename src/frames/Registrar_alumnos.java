    private void cargarTabla(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Grado");
        modelo.addColumn("Telefono");
        modelo.addColumn("Materia");

        tablaregistroalumno.setModel(modelo);

        // 1. Modificamos el SQL para incluir id_docente
        // Usamos AND (CondicionBusqueda) para que se cumplan ambos filtros
        String sql = "SELECT a.id_alumno, a.nombre, a.apellidos, a.grado, a.telefono, c.nombre_curso "
                + "FROM alumnos a "
                + "INNER JOIN curso c ON a.id_curso_asignado = c.id_curso "
                + "WHERE a.id_docente = ? AND (a.nombre LIKE ? OR a.apellidos LIKE ?)";

        try {
            Connection con = Conectar.conexion();
            PreparedStatement ps = con.prepareStatement(sql);

            // 2. Pasamos el ID del docente logueado (el que guardamos en el Login)
            ps.setInt(1, Login.idUsuarioLogeado);

            // 3. Los parámetros de búsqueda pasan a ser el 2 y el 3
            ps.setString(2, "%" + valor + "%");
            ps.setString(3, "%" + valor + "%");

            ResultSet rs = ps.executeQuery();
            int contador = 0;

            while (rs.next()) {
                Object[] fila = new Object[6];
                fila[0] = rs.getInt(1);
                fila[1] = rs.getString(2);
                fila[2] = rs.getString(3);
                fila[3] = rs.getString(4);
                fila[4] = rs.getString(5);
                fila[5] = rs.getString(6);

                modelo.addRow(fila);
                contador++;
            }

            txtcantidad.setText(String.valueOf(contador));

            rs.close();
            ps.close();
            con.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage());
        }
    }

    private void cargarComboMateria() {

        String sql = "SELECT id_curso, nombre_curso FROM curso";

        try {
            Connection con = Conectar.conexion();
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            cmbmateria.removeAllItems();
            cmbmateria.addItem("Seleccione materia:");

            while (rs.next()) {
                cmbmateria.addItem(rs.getString("nombre_curso"));
            }

            rs.close();
            ps.close();
            con.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar materias: " + e.getMessage());
        }
    }

